public class Maximum {
    public static void main(String[] args) {
        int x = 5;
        int y = 10;

        if(x > y)
            System.out.println("The maximum is " + x);

        if(y > x)
            System.out.println("The maximum is " + y);

        if(y == x)
            System.out.println("The numbers are equal");
    }
}
